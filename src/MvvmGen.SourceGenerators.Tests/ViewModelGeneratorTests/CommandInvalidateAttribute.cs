using Xunit;

namespace MvvmGen.SourceGenerators
{
    public class CommandInvalidateAttributeTests : ViewModelGeneratorTestsBase
    {
        [InlineData(false, "[CommandInvalidate(nameof(FirstName))]")]
        [InlineData(false, "[CommandInvalidate(\"FirstName\")]")]
        [InlineData(true, "[CommandInvalidate(nameof(FirstName))]")]
        [InlineData(true, "[CommandInvalidate(\"FirstName\")]")]
        [Theory]
        public void RaiseCanExecuteChangedInFirstNameProperty(bool putAttributeOnExecuteMethod, string commandInvalidateAttribute)
        {
            ShouldGenerateExpectedCode(
      $@"using MvvmGen;

namespace MyCode
{{
  [ViewModel]
  public partial class EmployeeViewModel
  {{
    [Property] string _firstName;
    [Property] string _lastName;
    
    {(putAttributeOnExecuteMethod ? commandInvalidateAttribute : "")}
    [Command(nameof(CanSave))]
    public void Save() {{ }}

    {(putAttributeOnExecuteMethod ? "" : commandInvalidateAttribute)}
    public bool CanSave() => true;
  }}
}}",
      $@"{AutoGeneratedComment}
{AutoGeneratedUsings}

namespace MyCode
{{
    partial class EmployeeViewModel : ViewModelBase
    {{
        public EmployeeViewModel()
        {{
            this.InitializeCommands();
            this.OnInitialize();
        }}

        partial void OnInitialize();

        private void InitializeCommands()
        {{
            SaveCommand = new(_ => Save(), _ => CanSave());
        }}

        public DelegateCommand SaveCommand {{ get; private set; }}

        public string FirstName
        {{
            get => _firstName;
            set
            {{
                if (_firstName != value)
                {{
                    _firstName = value;
                    OnPropertyChanged(""FirstName"");
                    SaveCommand.RaiseCanExecuteChanged();
                }}
            }}
        }}

        public string LastName
        {{
            get => _lastName;
            set
            {{
                if (_lastName != value)
                {{
                    _lastName = value;
                    OnPropertyChanged(""LastName"");
                }}
            }}
        }}
    }}
}}
");
        }
    }
}
